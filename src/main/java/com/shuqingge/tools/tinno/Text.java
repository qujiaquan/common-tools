    public static void processAndOutput(String[] key1, String[] value1, String[] key2, String[] value2) {
        List<String> remainingKey1 = new ArrayList<>();
        List<String> remainingValue1 = new ArrayList<>();
        List<String> remainingKey2 = new ArrayList<>();
        List<String> remainingValue2 = new ArrayList<>();

        // 遍历key1数组
        for (int i = 0; i < key1.length; i++) {
            String k1 = key1[i];
            String v1 = value1[i];

            // 检查k1是否在key2中
            boolean found = false;
            for (int j = 0; j < key2.length; j++) {
                if (k1.equals(key2[j])) {
                    String v2 = value2[j];
                    // 输出符合条件的内容
                    System.out.printf("%s\t%s\t%s\n", k1, v1, v2);
                    // 删除匹配的元素
                    key2[j] = null;
                    value2[j] = null;
                    found = true;
                    break;
                }
            }

            if (!found) {
                // 如果k1在key2中找不到，则保留下来
                remainingKey1.add(k1);
                remainingValue1.add(v1);
            }
        }

        // 输出剩余的key1和剩余的key2及其对应的value2
        System.out.println("Remaining key1:");
        for (int i = 0; i < remainingKey1.size(); i++) {
            String k1 = remainingKey1.get(i);
            String v1 = remainingValue1.get(i);
            System.out.printf("%s\t%s\n", k1, v1); // 输出key1和value1
        }

        System.out.println("Remaining key2:");
        for (int i = 0; i < key2.length; i++) {
            if (key2[i] != null) {
                System.out.printf("%s\t%s\n", key2[i], value2[i]); // 输出key2和value2
            }
        }
    }
